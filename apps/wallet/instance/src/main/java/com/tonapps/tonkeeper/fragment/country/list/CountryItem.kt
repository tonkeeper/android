package com.tonapps.tonkeeper.fragment.country.list

import com.tonapps.tonkeeper.fragment.country.CountryScreenFeature

data class CountryItem(
    val code: String,
    val title: String,
    val emoji: String,
    val selected: Boolean,
    override val position: com.tonapps.uikit.list.ListCell.Position
): com.tonapps.uikit.list.BaseListItem(), com.tonapps.uikit.list.ListCell {

    constructor(
        country: CountryScreenFeature.Country,
        selected: Boolean,
        position: com.tonapps.uikit.list.ListCell.Position
    ) : this(
        country.code,
        country.title,
        country.emoji,
        selected,
        position
    )
}